# Antecessor e Sucessor
n = int(input('Digite um número: '))
A = n - 1
S = n + 1
print('O antecessor do número {} é {}.\nE seu sucessor é {}.'.format(n, A, S))

# Outra maneira criando apenas 1 variável.

n = int(input('Digite um número: '))
print('O antecessor do número {} é {}\nE seu sucessor é {}.'.format(n, (n-1), (n+1)))

