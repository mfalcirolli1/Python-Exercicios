# Estruturas matemáticas

n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))
s = n1 + n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
print('A SOMA é {}!\nO PRODUTO é {}!\nE a DIVISÃO é {:.1f}!'.format(s, m, d), end=' >>> ')
print('Divisão inteira é {}, Potência é {}.'.format(di, e))


